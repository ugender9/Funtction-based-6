def classify_server_load(cpu_readings):
    """
    Classifies server load based on CPU usage readings.

    Parameters:
    cpu_readings (list): List of CPU usage percentages

    Returns:
    dict: Contains average CPU load and server status
    """

    # Calculate average CPU load
    avg_cpu = sum(cpu_readings) / len(cpu_readings)

    # Classify based on rules
    if avg_cpu < 50:
        status = "Normal"
    elif avg_cpu <= 80:
        status = "Warning"
    else:
        status = "Critical"

    return {
        "Average CPU Load": f"{avg_cpu:.0f}%",
        "Server Status": status,
        "Readings": cpu_readings,
        "Max Load": f"{max(cpu_readings)}%",
        "Min Load": f"{min(cpu_readings)}%"
    }

