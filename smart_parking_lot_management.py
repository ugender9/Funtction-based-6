# ===== PROBLEM 5: Smart Classroom Resource Usage Monitor =====

def monitor_classroom_resources(resource_usage):
    """
    Tracks classroom resource usage and identifies overuse patterns.

    Parameters:
    resource_usage (dict): Dictionary with resource names and usage hours

    Returns:
    dict: Contains overused resources and energy alert status
    """

    # Define resource limits (in hours)
    resource_limits = {
        "Projector": 6,
        "AC": 8,
        "Lights": 5
    }

    # Find overused resources
    overused_resources = []
    total_overuse = 0

    for resource, usage in resource_usage.items():
        limit = resource_limits.get(resource, 0)
        if usage > limit:
            overused_resources.append(resource)
            total_overuse += (usage - limit)

    # Determine energy alert
    energy_alert = "Yes" if total_overuse > 2 else "No"

    return {
        "Overused Resources": overused_resources if overused_resources else "None",
        "Energy Alert": energy_alert,
        "Total Overuse Hours": total_overuse,
        "Resource Usage": resource_usage
    }

