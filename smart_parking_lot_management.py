def estimate_delivery_time(distance, traffic_level, weather):
    """
    Estimates delivery time based on distance, traffic, and weather.

    Parameters:
    distance (float): Distance in kilometers
    traffic_level (str): 'Low', 'Medium', 'High'
    weather (str): 'Clear', 'Rainy', 'Stormy'

    Returns:
    dict: Contains estimated delivery time and status
    """

    # Base delivery time: 5 minutes + 5 minutes per km
    base_time = 5 + (distance * 5)

    # Traffic delays
    traffic_multiplier = 1.0
    if traffic_level == "Low":
        traffic_multiplier = 1.0
    elif traffic_level == "Medium":
        traffic_multiplier = 1.25
    elif traffic_level == "High":
        traffic_multiplier = 1.5

    # Weather delays
    weather_delay = 0
    if weather == "Clear":
        weather_delay = 0
    elif weather == "Rainy":
        weather_delay = 10
    elif weather == "Stormy":
        weather_delay = 20

    # Calculate final time
    final_time = int(base_time * traffic_multiplier + weather_delay)

    return {
        "Estimated Delivery Time": f"{final_time} minutes",
        "Distance": f"{distance} km",
        "Traffic Impact": traffic_level,
        "Weather Impact": weather
    }

