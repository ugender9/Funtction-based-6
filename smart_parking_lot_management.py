# ===== PROBLEM 6: Online Event Registration Capacity Controller =====

def manage_event_registrations(capacity, registrations):
    """
    Manages event registrations and handles overbooking.

    Parameters:
    capacity (int): Maximum event capacity
    registrations (int): Total number of registrations

    Returns:
    dict: Contains confirmed registrations, waitlisted users, and status
    """

    # Calculate confirmed and waitlisted registrations
    confirmed_registrations = min(capacity, registrations)
    waitlisted_users = max(0, registrations - capacity)

    # Determine registration status
    if registrations > capacity:
        status = "Closed"
    elif registrations == capacity:
        status = "Full"
    else:
        status = "Open"

    return {
        "Confirmed Registrations": confirmed_registrations,
        "Waitlisted Users": waitlisted_users,
        "Registration Status": status,
        "Event Capacity": capacity,
        "Total Registrations": registrations
    }

