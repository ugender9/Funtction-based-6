# Smart Parking Lot Management System

def manage_parking(capacity, logs):
    """
    Manages a smart parking lot system.

    Parameters:
    capacity (int): Maximum parking capacity
    logs (list): List of vehicle entry/exit logs ("IN" or "OUT")

    Returns:
    dict: Contains currently parked vehicles and parking status
    """

    # Initialize variables
    current_parked = 0
    peak_usage = 0
    parking_exceeded = False

    # Process each log entry
    for log in logs:
        if log == "IN":
            current_parked += 1
            # Update peak usage if current parked exceeds it
            if current_parked > peak_usage:
                peak_usage = current_parked
            # Check if parking exceeds capacity
            if current_parked > capacity:
                parking_exceeded = True
        elif log == "OUT":
            current_parked -= 1

    # Determine parking status
    if parking_exceeded:
        status = "Full - Capacity Exceeded"
    elif current_parked >= (capacity * 0.8):  # 80% capacity
        status = "Almost Full"
    elif current_parked > 0:
        status = "Available"
    else:
        status = "Empty"

    # Return results
    return {
        "Currently Parked Vehicles": current_parked,
        "Peak Parking Usage": peak_usage,
        "Parking Status": status,
        "Capacity Exceeded": parking_exceeded
    }

