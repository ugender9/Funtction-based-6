# ===== PROBLEM 3: Movie Theatre Seat Occupancy Analyzer =====

def analyze_theatre_occupancy(total_seats, booked_seats):
    """
    Analyzes theatre seat booking data.

    Parameters:
    total_seats (int): Total seats in theatre
    booked_seats (list): List of booked seat indicators (1 = booked, 0 = empty)

    Returns:
    dict: Contains occupancy percentage and show status
    """

    # Count booked seats
    booked_count = sum(booked_seats)

    # Calculate occupancy percentage
    occupancy_percentage = (booked_count / total_seats) * 100

    # Determine show status
    if occupancy_percentage >= 100:
        status = "Housefull"
    elif occupancy_percentage >= 80:
        status = "Almost Full"
    elif occupancy_percentage >= 50:
        status = "Half Occupied"
    else:
        status = "Available"

    # Suggest additional shows
    suggest_new_show = occupancy_percentage >= 75

    return {
        "Occupancy": f"{occupancy_percentage:.0f}%",
        "Show Status": status,
        "Booked Seats": booked_count,
        "Available Seats": total_seats - booked_count,
        "Suggest New Show": suggest_new_show
    }

